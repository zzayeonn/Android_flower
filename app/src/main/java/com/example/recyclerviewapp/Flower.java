package com.example.recyclerviewapp;

public class Flower {
    int foto;
    String name, story;

    public Flower() {
        this.foto = foto;
        this.name = name;
        this.story = story;
    }

    public int getFoto(){
        return foto;
    }

    public void setFoto(int foto){
        this.foto = foto;
    }

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getStory(){
        return story;
    }

    public void setStory(String story){
        this.story = story;
    }

}
